    private void btn_estimateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_estimateActionPerformed
       
        
            
        
        if (!(tf_plinth_arate.getText().equals("") || tf_total_land_area.getText().equals("") || tf_max_gcover.getText().equals("")) ) {
           
            if(Float.parseFloat(tf_max_gcover.getText()) <= Float.parseFloat(tf_total_land_area.getText()) * 0.7){
            int ind = cb_floors.getSelectedIndex();
            switch (ind) {
                case 0:
                    floors = 1;
                    break;
                case 1:
                    floors = (float) 1.5;
                    break;
                case 2:
                    floors = 2;
                    break;
                case 3:
                    floors = (float) 2.5;
                    break;
                case 4:
                    floors = 3;
                    break;
                case 5:
                    floors = (float) 3.5;
                    break;
                case 6:
                    floors = 4;
                    break;
            }
        
            
            float est_cost = Float.parseFloat(tf_max_gcover.getText()) * floors * Float.parseFloat(tf_plinth_arate.getText());
           // ce_estimate.setText("Estimated Cost : Rs. " + est_cost);
           CostSplit cs = new CostSplit();
           cs.setVisible(true);
          // btn_estimate.setEnabled(false);
           cs.est(est_cost);
           
           
        } else {
            JOptionPane.showMessageDialog(null,"Maximum Plinth Area exceeded" );    
        }
        }
        else{
           JOptionPane.showMessageDialog(null, "Fields are empty"); 
        }
    }//GEN-LAST:event_btn_estimateActionPerformed

