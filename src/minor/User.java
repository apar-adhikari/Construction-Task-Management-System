    public static String getMD5(String input) {
        try {
            MessageDigest md = MessageDigest.getInstance("MD5");
            byte[] messageDigest = md.digest(input.getBytes());
            BigInteger number = new BigInteger(1, messageDigest);
            String hashtext = number.toString(16);
            // Now we need to zero pad it if you actually want the full 32 chars.
            while (hashtext.length() < 32) {
                hashtext = "0" + hashtext;
            }
            return hashtext;
        }
        catch (NoSuchAlgorithmException e) {
            throw new RuntimeException(e);
        }
    }
    
    
    
    
    
    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed

        Dbconnect fb = new Dbconnect();
        fb.connect();
        Connection con = fb.con;
        try {
            Statement stmt;
            stmt = con.createStatement();
            String user = lun.getText().trim();
            String pass = lpsw.getText().trim();
            
            String passmd=getMD5(pass);
            String sql = "select username,password from register where username='" + user + "' and password='" + passmd + "' ";
            ResultSet rs = stmt.executeQuery(sql);

            int count = 0;
            while (rs.next()) {
                count = count + 1;
            }
            if (count == 1) {
                Dbentry en = new Dbentry(lun.getText());
                en.setVisible(true);
                close();

            } else {
                JOptionPane.showMessageDialog(null, "invalid username/password");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);

        }


    }//GEN-LAST:event_loginActionPerformed

